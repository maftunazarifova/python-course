#### - 6.Python kengaytmalari. - ####

# Linting - bu potentsial xatolar uchun kodni tahlil qiladigan dasturni ishga tushirish jarayoni.

# Autocomplete - bu dastur foydalanuvchi kiritadigan kodning qolgan qismini bashorat qiladigan xususiyatdir.

# Unit Testing - bu kodning kichik bir qismining ishlashini tekshiradigan dastur. Ishlab chiquvchilar saytlar va ilovalarni muntazam ravishda yangilab turishadi, xususiyatlar qo'shadilar, kodni o'zgartiradilar va tahrir qiladilar, so'ngra hamma narsa qanday ishlashini tekshiradilar.

# Debugging - kompyuter dasturlari, dasturiy ta'minot yoki tizimlarda xatolarni (nuqsonlarni yoki to'g'ri ishlashiga to'sqinlik qiladigan muammolarni) topish va tuzatish jarayoni.

# Formatting - yozilgan kodlarni tushunish uchun oson holatga keltirish holati.

# Code Snippet - kerakli vaqtda kerakli kontekstda qayta-qayta ishlatilishi mumkin bo'lgan kod shabloni.

